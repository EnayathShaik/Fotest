package com.ir.form.trainingPartner;

public class TrainingPartnerSearch {
	/**Training partner name */
	private String trainingPartnerName;
	/**Training center name associated to training partner */
	private String trainingCenterName;
	/**Address, phone number and email id of training center */
	private String details;
	/**Contact person name */
	private String contactPerson;
	/**Course offered at the training center */
	private String courseName;
	/**
	 * @return the trainingPartnerName
	 */
	public String getTrainingPartnerName() {
		return trainingPartnerName;
	}
	/**
	 * @param trainingPartnerName the trainingPartnerName to set
	 */
	public void setTrainingPartnerName(String trainingPartnerName) {
		this.trainingPartnerName = trainingPartnerName;
	}
	/**
	 * @return the trainingCenterName
	 */
	public String getTrainingCenterName() {
		return trainingCenterName;
	}
	/**
	 * @param trainingCenterName the trainingCenterName to set
	 */
	public void setTrainingCenterName(String trainingCenterName) {
		this.trainingCenterName = trainingCenterName;
	}
	/**
	 * @return the details
	 */
	public String getDetails() {
		return details;
	}
	/**
	 * @param details the details to set
	 */
	public void setDetails(String details) {
		this.details = details;
	}
	/**
	 * @return the contactPerson
	 */
	public String getContactPerson() {
		return contactPerson;
	}
	/**
	 * @param contactPerson the contactPerson to set
	 */
	public void setContactPerson(String contactPerson) {
		this.contactPerson = contactPerson;
	}
	/**
	 * @return the courseName
	 */
	public String getCourseName() {
		return courseName;
	}
	/**
	 * @param courseName the courseName to set
	 */
	public void setCourseName(String courseName) {
		this.courseName = courseName;
	}

}
