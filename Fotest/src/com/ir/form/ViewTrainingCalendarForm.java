package com.ir.form;

public class ViewTrainingCalendarForm {

	private String trainingLab;
	private String courseName;
	private String trainingDate;
	private String type;
	private String noOfSeats;
	private String noOfEnrollment;
	private String trainerName;
	public String getNoOfSeats() {
		return noOfSeats;
	}
	public void setNoOfSeats(String noOfSeats) {
		this.noOfSeats = noOfSeats;
	}
	public String getNoOfEnrollment() {
		return noOfEnrollment;
	}
	public void setNoOfEnrollment(String noOfEnrollment) {
		this.noOfEnrollment = noOfEnrollment;
	}
	public String getTrainerName() {
		return trainerName;
	}
	public void setTrainerName(String trainerName) {
		this.trainerName = trainerName;
	}
	public String getTrainingLab() {
		return trainingLab;
	}
	public void setTrainingLab(String trainingLab) {
		this.trainingLab = trainingLab;
	}
	public String getCourseName() {
		return courseName;
	}
	public void setCourseName(String courseName) {
		this.courseName = courseName;
	}
	public String getTrainingDate() {
		return trainingDate;
	}
	public void setTrainingDate(String trainingDate) {
		this.trainingDate = trainingDate;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}

	
}
