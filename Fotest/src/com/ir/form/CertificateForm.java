package com.ir.form;

public class CertificateForm {
          
	private int id;
	private String trainingType;
	private String completionStatus;
	private String certificateAvailable;
	private String generateCertificate;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getTrainingType() {
		return trainingType;
	}
	public void setTrainingType(String trainingType) {
		this.trainingType = trainingType;
	}
	public String getCompletionStatus() {
		return completionStatus;
	}
	public void setCompletionStatus(String completionStatus) {
		this.completionStatus = completionStatus;
	}
	public String getCertificateAvailable() {
		return certificateAvailable;
	}
	public void setCertificateAvailable(String certificateAvailable) {
		this.certificateAvailable = certificateAvailable;
	}
	public String getGenerateCertificate() {
		return generateCertificate;
	}
	public void setGenerateCertificate(String generateCertificate) {
		this.generateCertificate = generateCertificate;
	}
	
	
}
