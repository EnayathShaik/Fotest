package com.ir.form;

public class viewAssessmentAgencyCalendarForm {
	
	private int courseType;
	private int courseName ;
	private String assessmentDateTime;
	private String assessmentAgency;
	private String assessor;
	private String status;

	
	public int getCourseType() {
		return courseType;
	}
	public void setCourseType(int courseType) {
		this.courseType = courseType;
	}
	public int getCourseName() {
		return courseName;
	}
	public void setCourseName(int courseName) {
		this.courseName = courseName;
	}
	public String getAssessmentDateTime() {
		return assessmentDateTime;
	}
	public void setAssessmentDateTime(String assessmentDateTime) {
		this.assessmentDateTime = assessmentDateTime;
	}
	public String getAssessmentAgency() {
		return assessmentAgency;
	}
	public void setAssessmentAgency(String assessmentAgency) {
		this.assessmentAgency = assessmentAgency;
	}
	public String getAssessor() {
		return assessor;
	}
	public void setAssessor(String assessor) {
		this.assessor = assessor;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	

}
